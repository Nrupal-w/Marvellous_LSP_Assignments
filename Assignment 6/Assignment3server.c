int Addition(int a ,int b) 
{
    int ans=0;
    ans=a+b;
    return ans;
}
int subtraction(int a,int b)
{
    int ans=0;
    if (a>b)
    {
        ans=a-b;
    }
    else
    {
        ans=b-a;
    }
    return ans;
}
int Multiplication(int a,int b)
{
    int ans=0;
    ans=a*b;
    return ans;
}
int division(int a,int b)
{
    int ans=0;
    ans=a/b;
    return ans;
}